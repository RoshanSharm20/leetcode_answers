Node* findIntersection(Node* head1, Node* head2)
{
    map<int,int> nodes;
    Node *p=head1,*q=head2;
    while(p)
    {
        nodes[p->data]+=1;
        p=p->next;
    }
    while(q)
    {
        nodes[q->data]+=1;
        q=q->next;
    }
    
    Node *dummy=new Node(-1);
    Node *ptr=dummy;
    for(auto it:nodes)
    {
        while(it.second>1)
        {
            Node *newnode = new Node(it.first);
            it.second-=2;
            ptr->next=newnode;
            ptr=ptr->next;
        }
    }
    return dummy->next;
}